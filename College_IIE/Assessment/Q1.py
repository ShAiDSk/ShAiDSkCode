# 1. Write a program to demonstrate different number data types in Python.

# Integer Data type
a = 12
print(f"a is type of {type(a)}\n") ## Output - a is type of <class 'int'> 

# Float Data type
f = 13.5
print(f"f is type of {type(f)}\n") ## Output - f is type of <class 'float'>

# Complex Number
c = 2.04j
print(f"c is type of {type(c)}\n") ## c is type of <class 'complex'>
