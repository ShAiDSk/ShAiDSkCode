# 5. Write a program to demonstrate working with tuples in python.

fruits = ["Apple", "Banana", "Mango", "Orange", "cherry"]
print("The tuples is: ", fruits)
print("printing the second fruits: ", fruits[1])
print("printing from 2-5", fruits[1:4])
print("List of all items in Tuple: ")
for i in fruits:
    print(i)
if "Apple" in fruits:
    print("Yes, 'apple is in the fruits tuple")
print("length of the tuple is: ", len(fruits))
