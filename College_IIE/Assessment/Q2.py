# 2. Write a program to perform different Arithmetic Operations on numbers in Python.
a = int(input("Enter the value of a\n"))
b = int(input("Enter the value of b\n"))

print(f"Addition of a and b: {a + b}\n")
print(f"Subtraction of a and b: {a - b}\n")
print(f"Multiplication of a and b: {a * b}\n")
print(f"Division of a and b: {a / b}\n")
print(f"Remainder of a and b: {a % b}\n")
print(f"Exponent of a and b: {a ** b}\n") # or power like 3 ^ 3 = 9